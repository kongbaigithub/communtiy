package com.hxl.community3.dto;

public class GithubUser {

    private String name;
    private Long id;
    private String biol;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBiol() {
        return biol;
    }

    public void setBiol(String biol) {
        this.biol = biol;
    }
}
